                              <h1>Funções e deveres da Secretaria de Meio Ambiente</h1>
                              </hgroup>
                         
                         <p>
                              No Brasil, está a cargo das prefeituras implantar e gerencias os projetos de arborização. Cabe a cada município distribuir os riscos e benefícios da urbanização, induzindo um desenvolvimento mais inclusivo e sustentável. Em nossa cidade, a SEMAM - Secretaria Municipal do Meio Ambiente de Macapá, é o departamento responsável por cumprir este papel. São suas missões: “Proporcionar, promover e desenvolver as políticas de meio ambiente do município e normas e padrões para a sua proteção, defender e controlar, bem como verificar o seu cumprimento, em articulação com os sistemas estadual e federal de meio ambiente; Promover o zoneamento ambiental do Município, como a aplicação do geoprocessamento das informações decorrentes.”
                         </p>

                         <p>
                              Dentro da SEMAM, existe ainda um departamento que cuida em específico de Paisagismo e Arborização, responsável por: “(...) elaborar, coordenar e supervisionar a execução de programas, projetos e atividades de paisagismo, administração do Horto Municipal e arborização, visando promover ações destinadas à melhoria da qualidade ambiental do município (...), e emitir relatórios mensal e trimensal, enviando ao Secretário” (art. 253 da Lei Orgânica). 
                         <p>
                              
                         <p>
                              Visitamos o Horto Municipal para conhecer mais sobre as funções e principais dificuldades no dia a dia da SEMAM, bem como realizar uma entrevista com funcionários do departamento de arborização. Pudemos também verificar melhor a dinâmica de trabalho, avaliar o espaço e entender como andam os projetos de arborização em implantação no município.
                         </p>
                              
                         <p>
                              O local é bastante amplo, com diversos canteiros e mudas prontas, tanto de espécies arbóreas quanto plantas ornamentais. Este é um dos principais trabalhos realizados pela equipe da secretaria, que posteriormente cede mudas aos cidadãos interessados. Há também um orquidário, com mais de 380 espécies, sendo que metade são nativas da Amazônia. Este fica ao lado de uma área de ressaca, cuja humidade favorece o desenvolvimento das orquídeas. A sede desse departamento situa-se num galpão subdividido em outras salas onde funcionam chefia e administração. Ali são gerenciados os processos de solicitação de remoção de árvores, catalogação de espécies e desenvolvimento de novos projetos de arborização.
                         </p>

