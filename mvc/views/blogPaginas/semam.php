                         <p>
                              Por outro lado, constatou-se que a estrutura precisa urgente de mais funcionários, veículos e computadores, que facilitariam ainda mais a realização das tarefas diárias e propiciariam um trabalho mais efetivo de pesquisa de espécies. Inclusive, uma sugestão dada por um dos entrevistados foi a criação de um banco de dados que registrasse e servisse de monitoramento aos trabalhos realizados, de modo a terem prosseguimento efetivo por quem quer que assumisse a coordenação dos projetos, já que tal ferramenta inexiste. Há universidades e faculdades parceiras que já realizam trabalhos paralelos de pesquisa, de modo a suprir em partes essa carência da Secretaria. Somado a isto, o conhecimento acadêmico na área da programação poderia somar no sentido de solidificar e registrar tudo o que se tem feito pela arborização da cidade, como patrimônio de todos.
